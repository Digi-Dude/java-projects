public class Main {
    public static void main(String[] args) {

        System.out.println("Максимальное и минимальное значения Byte");
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);

        System.out.println("Максимальное и минимальное значения Short");
        System.out.println(Short.MAX_VALUE);
        System.out.println(Short.MIN_VALUE);

        System.out.println("Максимальное и минимальное значения Integer");
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.MIN_VALUE);

        System.out.println("Максимальное и минимальное значения Long");
        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.MIN_VALUE);

        System.out.println("Максимальное и минимальное значения Float");
        System.out.println(Float.MAX_VALUE);
        System.out.println(-Float.MAX_VALUE);

        System.out.println("Максимальное и минимальное значения Double");
        System.out.println(Double.MAX_VALUE);
        System.out.println(-Double.MAX_VALUE);

    }
}
